package VariableBasic;

public class swapingtwofn {
	
	public static void main(String[] args) {
		
		int A=10;
		int B=30;
        
        
        A =A+B;
        B =A-B;
        A =A-B;
        
        System.out.println("Print swap value of A="+A);
        System.out.println("Print swap value of B="+B);
        
        
	}

}
