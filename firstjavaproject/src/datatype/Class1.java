package datatype;

public class Class1 {
	public static void main(String[] args) {
		
		// decleration of 8 primitive data types 
		
		byte a=126; // byte can store the values from -128 to 127 
		
		short b=32767; // short can store the values from -32767 to 32768
		
		int c=1234567;// int store int no of digits having 10 digits only
		
		long d=7652342224244237889l; // long store the 19 digits no.
		
		boolean e= true;  // conditional data type only TRUE or FALSE
		
		char f='Z';
		
		double g= 12.2345674;//use for fractional number max 15digits.
		
		float h=78.656789f; // f is mandetory
		
		
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);

		
		
		
	}

}
